export type MessageKey = keyof (typeof messages)['en-US'];

export function resolveLocale(input?: string | null): Locale {
  if (!input) {
    return 'en-US';
  }

  const normalized = input.toLowerCase();
  if (normalized.startsWith('zh')) {
    return 'zh-CN';
  }
  return 'en-US';
}
